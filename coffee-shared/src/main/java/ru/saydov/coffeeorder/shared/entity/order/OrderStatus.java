package ru.saydov.coffeeorder.shared.entity.order;

/**
 * Статус заказа — этап его жизненного цикла в кофейне.
 *
 * <p>Статусы переключаются бариста в админском приложении или табло.
 * Переход однонаправленный: {@code PENDING → IN_PROGRESS → READY → DONE};
 * обратный переход возможен только через прямое редактирование админом.
 *
 * <p><b>Значения и их назначение:</b>
 * <ul>
 *   <li>{@link #PENDING} — заказ принят, но ещё не взят в работу</li>
 *   <li>{@link #IN_PROGRESS} — бариста готовит заказ</li>
 *   <li>{@link #READY} — заказ готов, ожидает выдачи клиенту</li>
 *   <li>{@link #DONE} — заказ выдан; терминальный статус</li>
 * </ul>
 */
public enum OrderStatus {

    /**
     * Заказ принят, но ещё не взят в работу.
     *
     * <p>Начальное состояние после создания заказа. На табло отображается
     * в секции «Новые». Клиент оплатил или использовал бонусы полностью.
     */
    PENDING,

    /**
     * Бариста готовит заказ.
     *
     * <p>Переходит в {@link #READY} по готовности. На табло отображается
     * в секции «Готовится».
     */
    IN_PROGRESS,

    /**
     * Заказ готов, ожидает выдачи клиенту.
     *
     * <p>Бариста вызывает клиента. На клиентском устройстве отправляется
     * SSE-уведомление о готовности.
     */
    READY,

    /**
     * Заказ выдан клиенту — терминальный статус.
     *
     * <p>Только при переходе в этот статус начисляются бонусные баллы
     * за оплаченную часть заказа.
     */
    DONE,
}
